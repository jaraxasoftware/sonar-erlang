-module(map_comprehension_test).

map_comprehension() ->
    M = #{I => I*I || I <- lists:seq(1, 7)},
    % => #{1 => 1,2 => 4,3 => 9,4 => 16,5 => 25,6 => 36,7 => 49}

    [{V, K} || K := V <- M],
    % => {1,1},{4,2},{9,3},{16,4},{25,5},{36,6},{49,7}]

    #{K => V*10 || K := V <- M},
    % => #{1 => 10,2 => 40,3 => 90,4 => 160,5 => 250,6 => 360,7 => 490}

    ok.
